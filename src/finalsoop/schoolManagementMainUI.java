    private void cacheResultSet(java.sql.ResultSet rs){
        if (rs == null) {
            System.out.println("ResultSet is null");
        }
        if (populatedTable == null) {
            System.out.println("populatedTable is null");
        }
        currentRs = rs;
        db.populateTable(currentRs, populatedTable);
    }

